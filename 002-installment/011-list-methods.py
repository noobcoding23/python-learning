# ----------List Methods in python------------

l = [1, 77, 12, 99, 17, 2]
print(l)
l.reverse()
print(l, "<- The list in reverse order")
l.append(7) # This method just adds a new data to a list
print(l, "<- Appended list")
l.sort()
print(l, "<- Sorted list low->high")
l.sort(reverse=True)
print(l, "<- Sorted list reverse order")

